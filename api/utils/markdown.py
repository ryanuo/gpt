import re


def compress_markdown(md: str) -> str:
    """将 Markdown 压缩成适合 LLM 的纯文本。"""

    # ----------------------------
    # 删除 YAML Front Matter
    # ----------------------------
    md = re.sub(
        r"^---\s*\n.*?\n---\s*\n?",
        "",
        md,
        flags=re.S,
    )

    # ----------------------------
    # 删除 HTML 注释
    # ----------------------------
    md = re.sub(
        r"<!--.*?-->",
        "",
        md,
        flags=re.S,
    )

    # ----------------------------
    # 图片 -> 保留 alt
    # ![alt](url) -> alt
    # ----------------------------
    md = re.sub(
        r"!\[(.*?)\]\((.*?)\)",
        r"\1",
        md,
    )

    # ----------------------------
    # 链接 -> 保留文字
    # [text](url) -> text
    # ----------------------------
    md = re.sub(
        r"\[(.*?)\]\((.*?)\)",
        r"\1",
        md,
    )

    # ----------------------------
    # 删除代码块围栏
    # ----------------------------
    md = re.sub(
        r"```[^\n]*",
        "",
        md,
    )

    md = md.replace("```", "")

    # ----------------------------
    # 行内代码
    # ----------------------------
    md = re.sub(
        r"`([^`]*)`",
        r"\1",
        md,
    )

    # ----------------------------
    # 标题
    # ----------------------------
    md = re.sub(
        r"^\s*#{1,6}\s*",
        "",
        md,
        flags=re.M,
    )

    # ----------------------------
    # 引用
    # ----------------------------
    md = re.sub(
        r"^\s*>\s*",
        "",
        md,
        flags=re.M,
    )

    # ----------------------------
    # 无序列表
    # ----------------------------
    md = re.sub(
        r"^\s*[-+*]\s+",
        "",
        md,
        flags=re.M,
    )

    # ----------------------------
    # 有序列表
    # ----------------------------
    md = re.sub(
        r"^\s*\d+\.\s+",
        "",
        md,
        flags=re.M,
    )

    # ----------------------------
    # 粗体
    # ----------------------------
    md = re.sub(
        r"\*\*(.*?)\*\*",
        r"\1",
        md,
    )

    md = re.sub(
        r"__(.*?)__",
        r"\1",
        md,
    )

    # ----------------------------
    # 斜体
    # ----------------------------
    md = re.sub(
        r"\*(.*?)\*",
        r"\1",
        md,
    )

    md = re.sub(
        r"_(.*?)_",
        r"\1",
        md,
    )

    # ----------------------------
    # 删除线
    # ----------------------------
    md = re.sub(
        r"~~(.*?)~~",
        r"\1",
        md,
    )

    # ----------------------------
    # 水平线
    # ----------------------------
    md = re.sub(
        r"^\s*[-*_]{3,}\s*$",
        "",
        md,
        flags=re.M,
    )

    # ----------------------------
    # HTML 标签
    # ----------------------------
    md = re.sub(
        r"<[^>]+>",
        "",
        md,
    )

    # ----------------------------
    # 合并所有换行为一个空格
    # ----------------------------
    md = md.replace("\r", "")
    md = md.replace("\n", " ")

    # ----------------------------
    # 合并所有空白字符
    # ----------------------------
    md = re.sub(
        r"\s+",
        " ",
        md,
    )

    return md.strip()